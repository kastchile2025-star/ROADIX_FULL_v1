function sanitizeNavbarUi() {
  document.querySelectorAll('.brand-copy').forEach((brandCopy) => {
    const strong = brandCopy.querySelector('strong');
    if (!strong) return;
    strong.textContent = 'ROADIX';
    brandCopy.replaceChildren(strong);
  });

  if (themeToggle) {
    const icon = themeToggle.querySelector('.theme-toggle-icon');
    themeToggle.childNodes.forEach((node) => {
      if (node !== icon) {
        node.remove();
      }
    });
  }
}

function getBillingValue(card, period, kind) {
  const suffix = period === 'annual' ? 'Annual' : 'Monthly';
  return card.dataset[`${kind}${suffix}`] || '';
}

function renderBillingPeriod() {
  const dict = translations[currentLang] || translations.es;
  billingPeriodButtons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.billingPeriod === currentBillingPeriod);
  });
  pricingCards.forEach((card) => {
    const priceValue = getBillingValue(card, currentBillingPeriod, 'priceValue');
    const priceSuffixKey = getBillingValue(card, currentBillingPeriod, 'priceSuffix');
    const priceNoteKey = getBillingValue(card, currentBillingPeriod, 'priceNote');
    const valueEl = card.querySelector('[data-price-value]');
    const suffixEl = card.querySelector('[data-price-suffix]');
    const noteEl = card.querySelector('[data-price-note]');

    if (valueEl) {
      valueEl.textContent = dict[priceValue] || priceValue;
    }
    if (suffixEl) {
      const suffixText = priceSuffixKey && dict[priceSuffixKey] ? dict[priceSuffixKey] : '';
      suffixEl.textContent = suffixText;
      suffixEl.hidden = !suffixText;
    }
    if (noteEl) {
      noteEl.textContent = priceNoteKey && dict[priceNoteKey] ? dict[priceNoteKey] : '';
    }
  });
}

function applyPageMeta(lang) {
  if (!pageKey || !pageMeta[pageKey]) return;
  const meta = pageMeta[pageKey];
  if (meta.title) {
    document.title = meta.title[lang] || meta.title.es;
  }
  if (metaDescription && meta.description) {
    metaDescription.setAttribute('content', meta.description[lang] || meta.description.es);
  }
}

function setTheme(theme) {
  currentTheme = theme;
  document.body.setAttribute('data-theme', theme);
  localStorage.setItem(storageKeys.theme, theme);
  if (themeToggleLabel) {
    themeToggleLabel.textContent = translations[currentLang][theme === 'dark' ? 'controls.themeDark' : 'controls.themeLight'];
  }
  if (themeToggleIcon) {
    themeToggleIcon.textContent = theme === 'dark' ? '◐' : '☀';
  }
  if (themeToggle) {
    themeToggle.setAttribute('aria-label', translations[currentLang][theme === 'dark' ? 'controls.themeDark' : 'controls.themeLight']);
  }
  sanitizeNavbarUi();
}

function setLanguage(lang) {
  currentLang = lang;
  const dict = translations[lang] || translations.es;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (key && dict[key]) {
      element.textContent = dict[key];
    }
  });
  document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
    const descriptor = element.getAttribute('data-i18n-attr');
    if (!descriptor) return;
    const [attribute, key] = descriptor.split(':');
    if (attribute && key && dict[key]) {
      element.setAttribute(attribute, dict[key]);
    }
  });
  if (langToggle) {
    langToggle.textContent = lang.toUpperCase();
  }
  localStorage.setItem(storageKeys.lang, lang);
  setTheme(currentTheme);
  applyPageMeta(lang);
  renderBillingPeriod();
}

function setActiveTab(target) {
  tabButtons.forEach((button) => {
    const isActive = button.dataset.tabTarget === target;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-selected', String(isActive));
  });
  tabPanels.forEach((panel) => {
    const isActive = panel.dataset.tabPanel === target;
    panel.hidden = !isActive;
    panel.classList.toggle('is-active', isActive);
  });
}

function markActiveNav() {
  document.querySelectorAll('[data-nav]').forEach((link) => {
    link.classList.toggle('is-active', link.getAttribute('data-nav') === pageKey);
  });
}

if (navToggle && mainNav) {
  navToggle.addEventListener('click', () => {
    const expanded = navToggle.getAttribute('aria-expanded') === 'true';
    navToggle.setAttribute('aria-expanded', String(!expanded));
    mainNav.classList.toggle('is-open');
  });
  mainNav.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      navToggle.setAttribute('aria-expanded', 'false');
      mainNav.classList.remove('is-open');
    });
  });
}

if (langToggle) {
  langToggle.addEventListener('click', () => {
    setLanguage(currentLang === 'es' ? 'en' : 'es');
  });
}

